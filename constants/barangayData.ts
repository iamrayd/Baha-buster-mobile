export interface BarangayData {
  id: string;
  name: string;
  coordinates: { latitude: number; longitude: number }[];
  riskLevel: 'none' | 'low' | 'medium' | 'high';
}

//Converts [lat, lng][] → { latitude, longitude }[]
export const barangayPolygons: BarangayData[] = Object.entries(BARANGAY_BOUNDARIES).map(
  ([name, coords]) => ({
    id: `brgy_${name.toLowerCase().replace(/\s+/g, '_')}`,
    name: formatBarangayName(name),
    coordinates: coords.map(([lat, lng]) => ({ latitude: lat, longitude: lng })),
    riskLevel: 'none', // Default - update from API
  })
);

/**
 * Format barangay name for display
 * "LAHUG" → "Lahug"
 */
function formatBarangayName(name: string): string {
  return name
    .split(' ')
    .map(word => word.charAt(0) + word.slice(1).toLowerCase())
    .join(' ');
}

/**
 * Update a specific barangay's risk level
 * Example: updateBarangayRisk('Lahug', 'high')
 */
export function updateBarangayRisk(
  barangayName: string,
  riskLevel: 'none' | 'low' | 'medium' | 'high'
): void {
  const barangay = barangayPolygons.find(
    b => b.name.toUpperCase() === barangayName.toUpperCase()
  );
  if (barangay) {
    barangay.riskLevel = riskLevel;
  }
}

/**
 * Bulk update from API response
 * Example: setRiskLevelsFromAPI({ "Lahug": "high", "Guadalupe": "medium" })
 */
export function setRiskLevelsFromAPI(
  riskData: Record<string, 'none' | 'low' | 'medium' | 'high'>
): void {
  Object.entries(riskData).forEach(([name, riskLevel]) => {
    updateBarangayRisk(name, riskLevel);
  });
}


